import { get as configGet, util as configUtil } from 'config';

export class AppSeeds {
  static SEED_COUNT = 400;

  static initiate() {
    if (configUtil.getEnv('NODE_ENV') === 'production') { return; }

    console.time('Inserting seed time');
    User.findOne({ username: 'Seeder'}, (err, seeder) => {
      if (seeder) {
        AppSeeds.populateSeeds(seeder);
        return;
      }
      const newSeeder = new User(<user>{
        'username': 'Seeder',
        'username_lower': 'seeder',
        'password': 'seedster',
      }).save((err, savedSeeder) => {
        if (err) { console.log(err); return; }
        AppSeeds.populateSeeds(savedSeeder);
      });
    });
  }






  private static populateSeeds(seeder: user) {
    const seeds: Partial<content>[] = [];
    for (let i = 0; i < AppSeeds.SEED_COUNT; i++) {
      const randomContent = contentTexts[Math.floor(Math.random() * contentTexts.length)];
      const randomFolder = folders[Math.floor(Math.random() * folders.length)];

      const content: Partial<content> = {
        title: 'AutoGenerated' + i,
        route: 'autogen' + i,
        content: `<p>${randomContent}</p>`,
        folder: randomFolder,
        access: accessRoles.everyone,
        createdBy: seeder._id,
        updatedBy: seeder._id,
      };
      seeds.push(content);
    }
    Content.insertMany(seeds, (err, done) => {
      if (err) {
        console.log(err);
        console.log('Issue inserting seeds');
      } else {
        console.log('Seeding complete');
      }
      console.time('Inserting seed time');
    });
  }

}


